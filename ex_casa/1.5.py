# Faça um programa que receba dois valores A e B.
# Faça a potência desses dois valores e retorna o resultado
# A ^ B = z

A = float(input("Insira um valor para A: "))
B = float(input("Insira um valor para B: "))

resultado = A ** B

print("\nO resultado da potência entre", A, "e", B,"=", resultado)